public class CommCdVO {
	private String cdId;
	private String cdNm;
	private String paCdId;
	private String userF;
	private String cdOrder;

	public String getCdId() {
		return cdId;
	}
	public void setCdId(String cdId) {
		this.cdId = cdId;
	}
	public String getCdNm() {
		return cdNm;
	}
	public void setCdNm(String cdNm) {
		this.cdNm = cdNm;
	}
	public String getPaCdId() {
		return paCdId;
	}
	public void setPaCdId(String paCdId) {
		this.paCdId = paCdId;
	}
	public String getUserF() {
		return userF;
	}
	public void setUserF(String userF) {
		this.userF = userF;
	}
	public String getCdOrder() {
		return cdOrder;
	}
	public void setCdOrder(String cdOrder) {
		this.cdOrder = cdOrder;
	}

}
